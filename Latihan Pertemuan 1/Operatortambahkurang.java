public class Operatortambahkurang {
    public static void main(String[] args) {
        int a, b;
        a = 7;
        b = 3;
        
        System.out.println("nilai a = " +a);
        System.out.println("nilai ++a = " + ++a);
        System.out.println("nilai a++ = " + a++);
        System.out.println("nilai a= " +a);
        System.out.println("------------------");
        System.out.println("nilai b = " +b);
        System.out.println("nilai ++b = " + ++b);
        System.out.println("nilai b++ = " + b++);
        System.out.println("nilai b= " +b);
        
        
    }
}
